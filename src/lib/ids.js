/* =============================================================
   lib/ids.js — 클라이언트 임시 id 생성 (계약 §1-4: {prefix}_{rand}).
   서버 도입 후 서버 발급 id 로 대체되며, 프론트는 id 형식에 의존하지
   않는다. 화면이 mock 내부(DB.uid)를 직접 import 하던 경계 위반
   (계약 §7-9)을 해소하는 단일 소스 — 화면·mock 모두 여기서 가져온다.
   ============================================================= */
export const uid = (p) => p + '_' + Math.random().toString(36).slice(2, 8);
export default uid;
